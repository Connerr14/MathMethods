    private void btnPowerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPowerActionPerformed
        // Adding the users input to variables.
        powerResponse1 = Double.parseDouble(lblPowerInput1.getText());
        powerResponse2 = Double.parseDouble(lblPowerInput2.getText());
        
        // Calculating the users answer using a pre-defined math method.
        powerAnswer = Math.pow(powerResponse1, powerResponse2);
        
        // Rounding the users answer.
        powerAnswer = powerAnswer * 100;
        powerAnswer = Math.round(powerAnswer);
        powerAnswer = powerAnswer/100;
        
        // Outputting the users answer to the GUI.
        lblPowerAnswer.setText(String.valueOf("The answer is: " + powerAnswer +
                "."));
    }//GEN-LAST:event_btnPowerActionPerformed

