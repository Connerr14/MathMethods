    private void btnLargeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLargeActionPerformed
        // Adding the users input to variables. 
        largeResponse1 = Double.parseDouble(txtLargeInput1.getText());
        largeResponse2 = Double.parseDouble(txtLargeInput2.getText());
        
        // Calculating the users answer using a pre-defined math method.
        largeAnswer = Math.max(largeResponse1, largeResponse2);
        
        // Outputting the users answer to the GUI.
        lblLargeAnswer.setText(String.valueOf("The answer is: " +
                largeAnswer + "."));   
    }//GEN-LAST:event_btnLargeActionPerformed

